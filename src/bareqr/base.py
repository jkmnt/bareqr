def glog(n: int):
    assert n >= 1
    return LOG_TABLE[n]


def gexp(n: int):
    return EXP_TABLE[n % 255]


class Polynomial:

    __slots__ = ("num",)

    def __init__(self, num: Iterable[int], shift: int):
        assert num

        num = list(num)

        offset = 0
        for offset in range(len(num)):
            if num[offset] != 0:
                break

        self.num = num[offset:] + [0] * shift

    @overload
    def __getitem__(self, index: int) -> int: ...

    @overload
    def __getitem__(self, index: slice) -> list[int]: ...

    def __getitem__(self, index: int | slice):
        return self.num[index]

    def __iter__(self):
        return iter(self.num)

    def __len__(self):
        return len(self.num)

    def __mul__(self, other: Polynomial):
        num = [0] * (len(self) + len(other) - 1)

        for i, item in enumerate(self):
            for j, other_item in enumerate(other):
                num[i + j] ^= gexp(glog(item) + glog(other_item))

        return Polynomial(num, 0)

    def __mod__(self, other: Polynomial):
        difference = len(self) - len(other)
        if difference < 0:
            return self

        ratio = glog(self[0]) - glog(other[0])

        num = [item ^ gexp(glog(other_item) + ratio) for item, other_item in zip(self, other, strict=False)]
        if difference:
            num.extend(self[-difference:])

        # recursive call
        return Polynomial(num, 0) % other


class RSBlock(NamedTuple):
    total_count: int
    data_count: int


def rs_blocks(version: int, correction: Correction):
    block = RS_BLOCK_TABLE[correction][version - 1]

    blocks: list[RSBlock] = []

    for count, total_count, data_count in chunked(block, 3):
        for _ in range(count):
            blocks.append(RSBlock(total_count, data_count))

    return blocks
